#Создание нейросети для умножения двух чисел
class MultiplyNet(nn.Module):
    def __init__(self):
        super(MultiplyNet, self).__init__()# Вызываем конструктор родительского класса
        # Создаем слои нейросети:
        # Вход: 2 нейрона (два числа для умножения)
        # Скрытый слой: 16 нейронов для обучения нелинейной зависимости
        self.fc1 = nn.Linear(2, 16) # Первый полносвязный слой: 2 входа, 16 выходов
        self.fc2 = nn.Linear(16, 8)   
        self.fc3 = nn.Linear(8, 1)
        self.relu = nn.ReLU()# Функция активации ReLU (отрицательные значения превращает в 0)

    def forward(self, x):
        x = self.relu(self.fc1(x))
        x = self.relu(self.fc2(x))
        x = self.fc3(x) # Применяем выходной слой (без активации, так как регрессия)
        return x

# Функция для генерации данных
def generate_data(n):
    X = []
    y = []
    for _ in range(n):
        x1 = random.uniform(0, 10)
        x2 = random.uniform(0, 10)
        X.append([x1, x2])
        y.append(x1 * x2)
    return torch.tensor(X, dtype=torch.float32), torch.tensor(y, dtype=torch.float32).view(-1, 1)

# Обучение модели
def train_model(train_size):
    model = MultiplyNet()
    criterion = nn.MSELoss()# Среднеквадратичная ошибка (для задач регрессии)

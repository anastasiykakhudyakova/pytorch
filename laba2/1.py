'''
Загрузка изображений. 
План создания и обучения нейронной сети:
1) ПОЛУЧЕНИЕ ДАННЫХ - процесс исследования, анализа и сборки необходимых данных. 
   Используем готовый набор данных CIFAR10.
2) ПРЕДОБРАБОТКА - преобразование изображений в числа (так как нейронные сети работают только с числами).
3) СОЗДАНИЕ МОДЕЛИ - архитектура нейронной сети.
4) ОБУЧЕНИЕ - процесс настройки весов модели на тренировочных данных.
5) ТЕСТИРОВАНИЕ - проверка качества модели на тестовых данных.

В этом уроке мы работаем с ПЕРВЫМ и ВТОРЫМ пунктами (получение и предобработка данных).

ВАЖНО: CIFAR10 содержит 50 000 тренировочных и 10 000 тестовых картинок, 
которые нужно скачать в локальную директорию, чтобы при каждом обучении не скачивать их заново.

ТРАНСФОРМАЦИЯ ИЗОБРАЖЕНИЙ:
- Каждое изображение состоит из пикселей, каждый пиксель имеет 3 составляющие: 
  R (красный), G (зеленый), B (синий)
- transforms.ToTensor() преобразует PIL-изображение (значения 0-255) в тензор PyTorch (значения 0.0-1.0)
- transforms.Normalize() нормализует значения по формуле: (x - mean) / std
- В нашем случае: (x - 0.5) / 0.5, что дает диапазон от -1 до +1

ПОЧЕМУ НОРМАЛИЗАЦИЯ ВАЖНА:
При работе с обычными числами до 255 мы обязательно поймаем переполнение в определенный момент обучения
и оно аварийно завершится. Для исключения переполнения числа уменьшаются до диапазона [-1, 1].

РАЗМЕР ИЗОБРАЖЕНИЙ: все картинки 32x32 пикселя для ускорения процесса обучения.
(Если взять картинки больше, процесс обучения будет дольше, возможно потребуется более 24 часов)

DATA LOADER (ПОСТАВЩИК ДАННЫХ):
- batch_size = 10 - загружаем модель на обучение пакетами по 10 изображений, а не всеми 10000 сразу
- shuffle=True - перемешивание данных, исключает обучение на последовательных данных,
  настраивает перемешивание для получения более оптимального варианта
- shuffle=False - для тестовых данных, чтобы в случае дополнительного обучения можно было оценить результат
  на тех же тестовых значениях (воспроизводимость результатов)
'''

'''
ЗАДАНИЕ:
Загрузите изображения из CIFAR10 так, как это делалось в уроке.
Обязательно примените трансформацию: transforms.Normalize((0.5, 0.5, 0.5), (0.5, 0.5, 0.5))
Подготовьте DataLoader для тренировочных и тестовых данных.
'''

# Импортируем основную библиотеку PyTorch для работы с тензорами и нейронными сетями
# Тензоры - это многомерные массивы, основа вычислений в PyTorch (аналог numpy, но с GPU)
import torch

# Импортируем datasets (готовые наборы данных) и transforms (инструменты для преобразования изображений)
# datasets содержит CIFAR10, MNIST, ImageNet и другие популярные датасеты
# transforms содержит функции для нормализации, изменения размера, обрезки, поворота и т.д.
from torchvision import datasets, transforms

# Импортируем DataLoader - инструмент для подачи данных в модель пакетами (batch'ами)
# DataLoader автоматически разбивает данные на батчи, перемешивает, загружает параллельно (num_workers)
from torch.utils.data import DataLoader


#ОПРЕДЕЛЕНИЕ ПУТИ И РАЗМЕРА ПАКЕТА
# Так как класс CIFAR10 содержит 10 000 картинок, нам лучше их скачать в локальную директорию,
# чтобы при каждом обучении нам не приходилось их скачивать.
# "./Data_10" означает папку Data_10 в текущей директории.
# При первом запуске данные скачаются сюда, при повторных - будут браться из этой папки.
root = "./Data_10"  # переменная (путь) до папки, куда сохраним скачанные изображения

# Определяем размер пакета (batch size) - количество изображений за одну итерацию обучения
# batch_size = 10 означает, что за один шаг градиентного спуска будут обработаны 10 изображений
# Это оптимально для большинства задач: достаточно для стабильного градиента, но не перегружает память
batch_size = 10


# ОПИСАНИЕ ТРАНСФОРМАЦИИ ИЗОБРАЖЕНИЙ
# Создаем цепочку преобразований с помощью transforms.Compose
# Compose позволяет объединить несколько трансформаций в одну последовательность
# Они будут применяться к каждому изображению в том порядке, в котором указаны
transformations = transforms.Compose([
    
    # ----- ТРАНСФОРМАЦИЯ 1: Преобразование в тензор -----
    # Что делает: берет PIL-изображение (значения пикселей 0..255) и преобразует в тензор PyTorch
    # Результат: значения становятся float в диапазоне 0.0..1.0
    # Также меняет размерность: из (Height, Width, Channels) -> (Channels, Height, Width)
    # Для CIFAR10: (32, 32, 3) -> (3, 32, 32)
    # Почему это важно: нейронные сети работают только с тензорами, а не с изображениями
    transforms.ToTensor(),
    
    # ----- ТРАНСФОРМАЦИЯ 2: Нормализация -----
    # Применяет формулу (x - mean) / std для каждого канала (R, G, B)
    # Параметры: (mean_R, mean_G, mean_B) = (0.5, 0.5, 0.5)
    #             (std_R, std_G, std_B) = (0.5, 0.5, 0.5)
    # Результат: значения из диапазона [0, 1] преобразуются в диапазон [-1, 1]
    transforms.Normalize((0.5, 0.5, 0.5), (0.5, 0.5, 0.5)) # transforms.Normalize – функция нормализации изображений.
    
    # КАК ПРОИСХОДИТ ВЫЧИСЛЕНИЕ НОРМАЛИЗАЦИИ:
    # Каждое изображение состоит из пикселей, и каждый пиксель имеет три составляющие: 
    # красную, зеленую и синюю.
    # Пример: у нас есть цвет (200, 150, 100):
    #   - 200 по красному, 150 по зеленому и 100 по синему
    # 
    # Шаг 1: Делим все значения на 255 (максимальное значение канала)
    #   200 / 255 ≈ 0.78, 150 / 255 ≈ 0.59, 100 / 255 ≈ 0.39
    # 
    # Шаг 2: Вычитаем средние значения (mean = 0.5)
    #   0.78 - 0.5 = 0.28, 0.59 - 0.5 = 0.09, 0.39 - 0.5 = -0.11
    # 
    # Шаг 3: Делим на стандартное отклонение (std = 0.5)
    #   0.28 / 0.5 = 0.56, 0.09 / 0.5 = 0.18, -0.11 / 0.5 = -0.22
    # 
    # Результат: значения в диапазоне от -1 до +1
    # Таким образом каждый пиксель преобразуется к нормализованному значению.
    # 
    # ПОЧЕМУ ЭТО ВАЖНО:
    # - Предотвращает переполнение при обучении
    # - Ускоряет сходимость градиентного спуска
    # - При работе с обычными числами до 255 мы обязательно поймаем переполнение 
    #   в определенный момент обучения, и оно аварийно завершится.
])

# Получаем сами данные. Будет два набора данных: тренировочный (для обучения модели) и тестовый.

# ЗАГРУЗКА ТРЕНИРОВОЧНЫХ ДАННЫХ
# Загружаем тренировочный набор данных CIFAR10
# train=True означает, что берем 50 000 изображений для обучения
# При первом запуске download=True скачает данные (около 160 МБ)
# При повторных запусках данные будут браться из папки root, скачивания не будет
train_set = datasets.CIFAR10(
    root=root,                  # Указываем папку для хранения данных
    train=True,                 # train=True = тренировочная выборка (50 000 изображений)
    transform=transformations,  # Применяем цепочку преобразований к каждому изображению
    download=True               # Скачиваем данные, если их нет в указанной папке
)


#СОЗДАНИЕ DATA LOADER ДЛЯ ТРЕНИРОВОЧНЫХ ДАННЫХ
# Создаем DataLoader - поставщика изображений для модели
# DataLoader будет подавать данные в модель порциями (batch'ами) по 10 изображений
# Это позволяет не загружать все 50 000 изображений в память одновременно
train_data_loader = DataLoader(
    train_set,                  # Передаем объект с тренировочными данными
    batch_size=batch_size,      # Размер пакета = 10 изображений за одну итерацию
    shuffle=True                # Перемешиваем данные перед каждой эпохой
                                # Это важно, чтобы модель не запоминала порядок изображений
                                # и не обучалась на последовательных данных
)


#ЗАГРУЗКА ТЕСТОВЫХ ДАННЫХ
# Загружаем тестовый набор данных CIFAR10
# train=False означает, что берем 10 000 изображений для тестирования
# Тестовые данные не используются при обучении, только для финальной оценки качества
test_set = datasets.CIFAR10(
    root=root,                  # Используем ту же папку (данные уже там)
    train=False,                # train=False = тестовая выборка (10 000 изображений)
    transform=transformations,  # Применяем те же преобразования (для согласованности)
    download=True               # При необходимости скачиваем (обычно скачивается вместе с тренировочными)
)


#СОЗДАНИЕ DATA LOADER ДЛЯ ТЕСТОВЫХ ДАННЫХ
# Создаем DataLoader для тестовых данных
# Важное отличие: shuffle=False (не перемешиваем!)
# Это нужно, чтобы при каждом запуске мы оценивали модель на одних и тех же изображениях
# Результаты тестирования будут воспроизводимыми и сравнимыми
test_data_loader = DataLoader(
    test_set,                   # Передаем объект с тестовыми данными
    batch_size=batch_size,      # Тот же размер пакета = 10 изображений
    shuffle=False               # Не перемешиваем тестовые данные
                                # Почему: если перемешать, то при каждом тестировании порядок будет разный,
                                # и результаты могут немного отличаться из-за порядка
)


# ПРОВЕРКА ЗАГРУЖЕННЫХ ДАННЫХ (ВЫВОД ИНФОРМАЦИИ)
# Выводим размер тренировочного набора
# Ожидаемый результат: 50000 изображений
print(f"Размер тренировочного набора: {len(train_set)} изображений")

# Выводим размер тестового набора
# Ожидаемый результат: 10000 изображений
print(f"Размер тестового набора: {len(test_set)} изображений")

# Выводим размер пакета
print(f"Batch size: {batch_size}")

# Выводим количество батчей в тренировочном DataLoader
# Формула: 50000 / 10 = 5000 батчей
# Именно столько итераций обучения будет за одну эпоху
print(f"Количество батчей в тренировке: {len(train_data_loader)}")

# Выводим количество батчей в тестовом DataLoader
# Формула: 10000 / 10 = 1000 батчей
print(f"Количество батчей в тесте: {len(test_data_loader)}")


#ПРИМЕР ОДНОГО БАТЧА ДЛЯ ВИЗУАЛИЗАЦИИ
# Превращаем DataLoader в итератор
# Итератор позволяет получать данные по одному элементу с помощью функции next()
data_iter = iter(train_data_loader)

# Извлекаем первый пакет данных (один батч)
# images - тензор с изображениями (10 штук)
# labels - тензор с метками классов (цифры от 0 до 9)
images, labels = next(data_iter)

# Выводим форму (размерность) тензора изображений
# Результат: torch.Size([10, 3, 32, 32])
# 10 - количество изображений в пакете (batch size)
# 3 - количество цветовых каналов (RGB)
# 32 - высота изображения в пикселях
# 32 - ширина изображения в пикселях
print(f"\nФорма одного батча изображений: {images.shape}")

# Выводим диапазон значений после нормализации
# Ожидаемый результат: min = -1.00, max = 1.00
# Это подтверждает, что нормализация сработала правильно
# Все значения находятся в диапазоне от -1 до +1, как мы и хотели
print(f"Диапазон значений после нормализации: [{images.min():.2f}, {images.max():.2f}]")

# Выводим метки первого батча для информации
print(f"Метки классов в первом батче: {labels.tolist()}")

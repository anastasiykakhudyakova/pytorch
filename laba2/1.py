# 2. ✅ Загрузили изображения из CIFAR10
# 3. ✅ Применили трансформацию ToTensor() + Normalize((0.5,0.5,0.5), (0.5,0.5,0.5))
# 4. ✅ Сформировали нормализованные тренировочные данные
# 5. ✅ Сформировали нормализованные тестовые данные
# 6. ✅ Создали DataLoader для тренировочных данных (batch_size=10, shuffle=True)
# 7. ✅ Создали DataLoader для тестовых данных (batch_size=10, shuffle=False)

# Функция загрузки и предобработки данных CIFAR-10
def load_cifar10_data(batch_size=10, root='./Data_10'):
    # Создаем цепочку трансформаций для предобработки изображений
    transformations = transforms.Compose([
        # Преобразуем PIL-изображение (значения 0-255) в тензор (значения 0.0-1.0)
        transforms.ToTensor(),
        # Нормализуем значения по формуле (x - mean)/std, приводим к диапазону [-1, 1]
        transforms.Normalize((0.5, 0.5, 0.5), (0.5, 0.5, 0.5))
    ])
    # Загружаем тренировочный набор данных CIFAR-10 (50 000 изображений)
    train_set = torchvision.datasets.CIFAR10(root=root, train=True, download=True, transform=transformations)
    # Создаем DataLoader для тренировочных данных с перемешиванием (shuffle=True)
    train_loader = DataLoader(train_set, batch_size=batch_size, shuffle=True)
    # Загружаем тестовый набор данных CIFAR-10 (10 000 изображений)
    test_set = torchvision.datasets.CIFAR10(root=root, train=False, download=True, transform=transformations)
    # Создаем DataLoader для тестовых данных без перемешивания (shuffle=False)
    test_loader = DataLoader(test_set, batch_size=batch_size, shuffle=False)
    # Возвращаем оба загрузчика данных
    return train_loader, test_loader


# Определяем класс пользовательской сверточной нейронной сети
class CustomCNN(nn.Module):
    # Конструктор класса - здесь определяем все слои сети
    def __init__(self):
        # Вызываем конструктор родительского класса nn.Module
        super(CustomCNN, self).__init__()
        
        # ПЕРВЫЙ СВЕРТОЧНЫЙ СЛОЙ: вход 3 канала (RGB), выход 12 каналов, ядро 3x3
        # padding=1 сохраняет размерность изображения (32x32 -> 32x32)
        self.conv1 = nn.Conv2d(3, 12, kernel_size=3, stride=1, padding=1)
        # СЛОЙ ПАРТИЙНОЙ НОРМАЛИЗАЦИИ: нормализует выходы первого сверточного слоя (12 каналов)
        self.bn1 = nn.BatchNorm2d(12)
        
        # ВТОРОЙ СВЕРТОЧНЫЙ СЛОЙ: вход 12 каналов (из первого слоя), выход 24 канала
        # ВАЖНО: количество входных каналов должно совпадать с выходными каналами предыдущего слоя!
        self.conv2 = nn.Conv2d(12, 24, kernel_size=3, stride=1, padding=1)  # Исправлено: 24 -> 12 на входе
        # СЛОЙ ПАРТИЙНОЙ НОРМАЛИЗАЦИИ: нормализует выходы второго сверточного слоя (24 канала)
        self.bn2 = nn.BatchNorm2d(24)
        
        # СЛОЙ МАКСИМУМ-ПУЛИНГА: уменьшает размерность в 2 раза (kernel_size=2, stride=2)
        self.pool = nn.MaxPool2d(kernel_size=2, stride=2)
        
        # ПОЛНОСВЯЗНЫЙ СЛОЙ: вход 24*8*8 (1536 признаков), выход 10 (классов)
        # Размер 8x8 получается после двух пулингов: 32 -> 16 -> 8
        self.fc = nn.Linear(24 * 8 * 8, 10)

    # Метод прямого распространения - определяем последовательность прохождения данных
    def forward(self, x):
        # БЛОК 1: свертка -> нормализация -> ReLU -> пулинг
        # Применяем первый сверточный слой к входным данным x (3 -> 12 каналов)
        x = self.conv1(x)  # Размер: [batch, 12, 32, 32]
        # Применяем партийную нормализацию для стабилизации обучения
        x = self.bn1(x)    # Размер: [batch, 12, 32, 32]
        # Применяем функцию активации ReLU (отрицательные значения обнуляются)
        x = F.relu(x)      # Размер: [batch, 12, 32, 32]
        # Применяем слой пулинга для уменьшения размерности (32x32 -> 16x16)
        x = self.pool(x)   # Размер: [batch, 12, 16, 16]
        
        # БЛОК 2: свертка -> нормализация -> ReLU -> пулинг
        # Применяем второй сверточный слой (12 -> 24 канала, сохраняем размер 16x16)
        x = self.conv2(x)  # Размер: [batch, 24, 16, 16]
        # Применяем партийную нормализацию
        x = self.bn2(x)    # Размер: [batch, 24, 16, 16]
        # Применяем ReLU
        x = F.relu(x)      # Размер: [batch, 24, 16, 16]
        # Применяем пулинг (16x16 -> 8x8)
        x = self.pool(x)   # Размер: [batch, 24, 8, 8]
        
        # ПОДГОТОВКА К ПОЛНОСВЯЗНОМУ СЛОЮ
        # Изменяем форму тензора: из [batch, 24, 8, 8] в [batch, 24*8*8]
        x = x.view(-1, 24 * 8 * 8)  # Размер: [batch, 1536]
        
        # Применяем полносвязный слой для получения 10 выходных значений (логиты классов)
        x = self.fc(x)  # Размер: [batch, 10]
        
        # Возвращаем выходные значения (без softmax, так как CrossEntropyLoss включает его)
        return x


# Функция для оценки точности модели на тестовых данных
def test_accuracy(model, test_loader):
    # Переводим модель в режим оценки (отключаем Dropout и BatchNorm для инференса)
    model.eval()
    # Счетчик правильных ответов
    correct = 0
    # Общее количество примеров
    total = 0
    # Отключаем вычисление градиентов для экономии памяти и ускорения
    with torch.no_grad():
        # Перебираем все батчи в тестовом загрузчике
        for images, labels in test_loader:
            # Получаем выходы модели для текущего батча изображений
            outputs = model(images)
            # Находим индекс класса с максимальным значением (прогноз)
            _, predicted = torch.max(outputs.data, 1)
            # Увеличиваем счетчик общего количества примеров
            total += labels.size(0)
            # Увеличиваем счетчик правильных ответов
            correct += (predicted == labels).sum().item()
    # Вычисляем точность в процентах
    accuracy = 100 * correct / total
    return accuracy


#Функция для визуализации изображений и вывода прогнозов модели
def show_images_with_predictions(model, test_loader, classes, num_images=20):
    # Переводим модель в режим оценки
    model.eval()
    
    # Получаем первый батч из тестового загрузчика (10 изображений по умолчанию)
    images, labels = next(iter(test_loader))
    # Берем только первые num_images изображений из батча
    images, labels = images[:num_images], labels[:num_images]
    
    # Получаем прогнозы модели для этих изображений (без вычисления градиентов)
    with torch.no_grad():
        outputs = model(images)
        # Находим индексы предсказанных классов
        _, predicted = torch.max(outputs, 1)
    
    # ВИЗУАЛИЗАЦИЯ ИЗОБРАЖЕНИЙ
    # Объединяем все изображения в одну сетку для отображения
    # nrow=5 означает 5 изображений в строке (4 строки по 5 = 20 изображений)
    grid_img = torchvision.utils.make_grid(images, nrow=5, padding=2)
    # Денормализуем изображения: обратное преобразование (x * 0.5 + 0.5)
    # Возвращаем значения из диапазона [-1, 1] в [0, 1]
    grid_img = grid_img / 2 + 0.5
    # Преобразуем тензор в массив numpy для отображения в matplotlib
    np_images = grid_img.numpy()
    # Транспонируем оси из (C, H, W) в (H, W, C) для отображения в matplotlib
    np_images = np.transpose(np_images, (1, 2, 0))
    
    # Создаем фигуру для отображения с указанным размером
    plt.figure(figsize=(12, 10))
    # Отображаем изображения
    plt.imshow(np_images)
    # Отключаем оси координат для чистого отображения
    plt.axis('off')
    # Устанавливаем заголовок для окна
    plt.title(f'CIFAR-10: 20 тестовых изображений с прогнозами модели', fontsize=14)
    # Показываем окно с изображениями
    plt.show()
    
    # ВЫВОД ПРАВИЛЬНЫХ ОТВЕТОВ В КОНСОЛЬ
    print("\n" + "="*60)
    print("РЕЗУЛЬТАТЫ ПРОГНОЗИРОВАНИЯ ДЛЯ 20 ИЗОБРАЖЕНИЙ")
    print("="*60)
    print(f"{'№':<4} {'Правильный ответ':<15} {'Прогноз нейросети':<20} {'Результат':<10}")
    print("-"*60)
    
    # Перебираем все 20 изображений
    for i in range(num_images):
        # Получаем правильную метку и прогноз для текущего изображения
        true_label = classes[labels[i]]
        pred_label = classes[predicted[i]]
        # Определяем, правильный ли прогноз
        is_correct = (predicted[i] == labels[i])
        # Выбираем символ для отображения результата
        result = "✓ Верно" if is_correct else "✗ Неверно"
        # Выводим информацию для текущего изображения
        print(f"{i+1:<4} {true_label:<15} {pred_label:<20} {result:<10}")
    
    # РАСЧЕТ ТОЧНОСТИ НА 20 ИЗОБРАЖЕНИЯХ
    # Подсчитываем количество правильных ответов
    correct_count = (predicted == labels).sum().item()
    # Вычисляем точность в процентах
    accuracy = 100 * correct_count / num_images
    # Выводим итоговую точность
    print("-"*60)
    print(f"ИТОГО: Правильных ответов: {correct_count} из {num_images}")
    print(f"ТОЧНОСТЬ: {accuracy:.2f}%")
    print("="*60 + "\n")
    
    # Возвращаем точность для дальнейшего использования
    return accuracy


# ОСНОВНАЯ ФУНКЦИЯ - ТОЧКА ВХОДА В ПРОГРАММУ
def main():
    # ШАГ 1: ЗАГРУЗКА ДАННЫХ
    # Загружаем тренировочный и тестовый загрузчики с размером пакета 10
    train_loader, test_loader = load_cifar10_data(batch_size=10)
    # ШАГ 2: СОЗДАНИЕ МОДЕЛИ
    # Создаем экземпляр нашей сверточной нейронной сети
    model = CustomCNN()
    
    # ШАГ 3: ОПРЕДЕЛЕНИЕ ФУНКЦИИ ПОТЕРЬ И ОПТИМИЗАТОРА
    # Функция потерь - кросс-энтропия (подходит для классификации)
    criterion = nn.CrossEntropyLoss()
    # Оптимизатор Adam с learning rate=0.001 и L2-регуляризацией (weight_decay)
    optimizer = optim.Adam(model.parameters(), lr=0.001, weight_decay=1e-5)
    # ШАГ 4: ОБУЧЕНИЕ МОДЕЛИ
    # Количество эпох обучения (полных проходов по всем данным)
    num_epochs = 10
    # Список для сохранения точности по эпохам
    epoch_accuracies = []
    
    # Цикл по эпохам обучения
    for epoch in range(num_epochs):
        # Переводим модель в режим обучения (включаем Dropout, BatchNorm работает в режиме обучения)
        model.train()
        # Переменная для накопления потерь за эпоху
        running_loss = 0.0
        
        # Цикл по батчам тренировочных данных
        for batch_idx, (images, labels) in enumerate(train_loader):
            # Обнуляем градиенты оптимизатора (накапливаемые от предыдущего шага)
            optimizer.zero_grad()
            # Прямой проход: получаем выходы модели для текущего батча
            outputs = model(images)
            # Вычисляем значение функции потерь между выходами и правильными метками
            loss = criterion(outputs, labels)
            # Обратный проход: вычисляем градиенты потерь по параметрам модели
            loss.backward()
            # Шаг оптимизатора: обновляем веса модели на основе градиентов
            optimizer.step()
            # Накопливаем значение потерь
            running_loss += loss.item()
        
        # Вычисляем среднюю потерю за эпоху
        avg_loss = running_loss / len(train_loader)
        
        # ОЦЕНКА ТОЧНОСТИ ПОСЛЕ КАЖДОЙ ЭПОХИ
        # Вызываем функцию тестирования точности на тестовых данных
        accuracy = test_accuracy(model, test_loader)
        epoch_accuracies.append(accuracy)
        
        # Выводим результат в консоль
        print(f"Эпоха {epoch + 1:2}/{num_epochs} | Потери: {avg_loss:.4f} | Точность: {accuracy:.2f}%")
       
    print(f"Финальная точность на тестовых данных: {epoch_accuracies[-1]:.2f}%")
    print(f"Максимальная точность: {max(epoch_accuracies):.2f}%")
    
    # ШАГ 5: ВИЗУАЛИЗАЦИЯ РЕЗУЛЬТАТОВ
    # Список названий классов CIFAR-10 на русском языке
    classes = ('самолет', 'автомобиль', 'птица', 'кот', 'олень', 
               'собака', 'лягушка', 'лошадь', 'корабль', 'грузовик')
    
    # Вызываем функцию отображения 20 изображений с прогнозами модели
    final_accuracy = show_images_with_predictions(model, test_loader, classes, num_images=20)
    
    # График обучения: отображение точности по эпохам
    plt.figure(figsize=(10, 6))
    plt.plot(range(1, num_epochs + 1), epoch_accuracies, 'b-', marker='o', linewidth=2, markersize=8)
    plt.title('Динамика точности модели в процессе обучения', fontsize=14)
    plt.xlabel('Эпоха', fontsize=12)
    plt.ylabel('Точность (%)', fontsize=12)
    plt.grid(True, alpha=0.3)
    plt.ylim(0, 100)
    
    # Добавляем значения точности на график
    for i, acc in enumerate(epoch_accuracies):
        plt.text(i + 1, acc + 1, f'{acc:.1f}%', ha='center', fontsize=9)
    
    plt.tight_layout()
    plt.show()



import torch
import torch.nn as nn # Импортируем модуль для создания слоев нейронной сети (Linear, Conv2d, BatchNorm2d, MaxPool2d)
import torch.nn.functional as F # Импортируем функциональный модуль с функциями активации (relu) и другими операциями

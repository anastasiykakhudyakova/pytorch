'''Объявим функцию, которая вернет нам точность предсказания (по верно угаданным отзывам).
Во-первых, выходные значения от нейросети представляют собой числа с плавающей запятой в диапазоне от 0 до 1. 
Так как верные значения – это labels – представленные 0 или 1, то для процедуры сравнения, мы должны преобразовать выходные значения к 0 и 1.
Для этого переберем в цикле полученные значения и сравним с 0.5. Если значение больше 0.5, то сохраняем 1 иначе 0.
Возвращаем среднее арифметическое полученных значений, преобразуя к числовому типу с плавающей запятой.'''
def accuracy(predictions, labels):
    """
    Функция для вычисления точности классификации
    
    Параметры:
    - predictions: предсказанные вероятности (тензор PyTorch)
    - labels: истинные метки (тензор PyTorch)
    
    Возвращает: точность (доля правильных ответов)
    """
    # Округляем вероятности до 0 или 1 (порог 0.5)
    # torch.round() - значения <0.5 -> 0, >=0.5 -> 1
    rounded_preds = torch.round(predictions)
    
    # Сравниваем с истинными метками, преобразуем в float (True->1, False->0)
    correct = (rounded_preds == labels).float()
    
    # Возвращаем долю правильных ответов
    return correct.sum() / len(correct)



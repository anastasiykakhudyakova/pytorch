
"""
ТЕОРИЯ: ЗАГРУЗКА И ОБРАБОТКА ОТЗЫВОВ ДЛЯ НЕЙРОСЕТИ
==================================================

1. ПОСТАНОВКА ЗАДАЧИ
-------------------
Создаём нейросеть, которая определяет настроение (sentiment) отзыва по его тексту.
Задача - бинарная классификация: отзыв либо положительный (positive), либо отрицательный (negative).

2. ДАННЫЕ ДЛЯ ОБУЧЕНИЯ
---------------------
Используем набор из 50 000 отзывов о фильмах в формате CSV.
Каждая запись содержит:
- Текст отзыва (с HTML-тегами, знаками препинания, цифрами)
- Метку настроения: 'positive' или 'negative'

Пример: "This movie was great! <br /> I loved it.",positive

3. ЭТАПЫ ПРЕДОБРАБОТКИ ТЕКСТА (NLP PIPELINE)
-------------------------------------------

3.1. ОЧИСТКА ТЕКСТА (Text Cleaning)
    - Удаление HTML-тегов (<br />, <p>, <div>)
    - Удаление знаков препинания (., !, ?, ;, :)
    - Удаление цифр и спецсимволов
    - Удаление лишних пробелов
    - Приведение к нижнему регистру

3.2. ТОКЕНИЗАЦИЯ (Tokenization)
    Разбиение текста на отдельные слова (токены)
    Пример: "I love movies" -> ["I", "love", "movies"]
    Инструмент: word_tokenize из библиотеки NLTK

3.3. УДАЛЕНИЕ СТОП-СЛОВ (Stop Words Removal)
    Стоп-слова - слова, не несущие смысловой нагрузки:
    - Предлоги: in, on, at, for
    - Местоимения: I, you, he, she, it
    - Союзы: and, or, but, so
    - Вспомогательные глаголы: is, am, are, was, were
    
    Инструмент: stopwords.words('english') из NLTK (179 стоп-слов)

3.4. ЛЕММАТИЗАЦИЯ (Lemmatization)
    Приведение слова к нормальной словарной форме (лемме):
    
    | Словоформа | Лемма    |
    |------------|----------|
    | going      | go       |
    | better     | good     |
    | mice       | mouse    |
    | children   | child    |
    | am, are    | be       |
    
    Инструмент: WordNetLemmatizer из NLTK (на основе WordNet)
    
    ВАЖНО: После лемматизации некоторые слова могут стать стоп-словами.
    Например: 'yours' -> 'you', где 'you' - стоп-слово.
    Поэтому очистку от стоп-слов нужно выполнить ПОВТОРНО.

4. ФИНАЛЬНЫЙ ФОРМАТ ДАННЫХ
-------------------------
После всех преобразований:
- Столбец 'processed': обработанная строка из слов в начальной форме
- Столбец 'label': 1 для положительных отзывов, 0 для отрицательных

5. СОХРАНЕНИЕ РЕЗУЛЬТАТОВ
------------------------
Результат сохраняется в CSV файл 'reviews_preprocessed.csv', чтобы:
- Не тратить время на повторную обработку при обучении нейросети
- Иметь готовые данные для моделирования

6. ПРОГРЕСС-БАР
--------------
Используется библиотека tqdm для визуального отслеживания прогресса:
progress_apply() вместо apply() показывает процесс обработки отзывов
"""


'''Задание
Скопируйте из исходников файл reviews.csv в свой проект.
Подготовьте данные для обучения, как на занятии: оставьте только буквы и пробелы (без лишних пробелов), 
проведите лемматизацию слов, удалите стоп-слова.
Сохраните обработанные отзывы в файл reviews_preprocessed.csv со столбцами: review и label. 
Примечание: в столбце label должен быть 1, если отзыв позитивный и 0, если он негативный.
'''

import re  # Импортируем модуль для работы с регулярными выражениями (очистка текста от тегов, цифр, знаков)
import nltk  # Импортируем библиотеку для обработки естественного языка (токенизация, стоп-слова, лемматизация)
import pandas as pd  # Импортируем pandas для работы с DataFrame (чтение/запись CSV файлов)
from tqdm import tqdm  # Импортируем tqdm для отображения прогресс-бара при обработке
from nltk.tokenize import word_tokenize  # Импортируем функцию для разбиения текста на отдельные слова (токены)
from nltk.corpus import stopwords  # Импортируем набор стоп-слов (предлоги, местоимения и т.д.)
from nltk.stem import WordNetLemmatizer  # Импортируем класс для лемматизации (приведение к начальной форме)

# Скачиваем необходимые данные NLTK (выполняется один раз при первом запуске)
nltk.download('punkt')  # Скачиваем модель для токенизации (разбиение текста на слова/предложения)
nltk.download('punkt_tab')  # Скачиваем дополнительную таблицу для токенизации (решение ошибок с punkt)
nltk.download('stopwords')  # Скачиваем словарь стоп-слов для английского языка
nltk.download('wordnet')  # Скачиваем лексическую базу WordNet для лемматизации

# Создаём глобальные объекты для оптимизации (чтобы не создавать их заново для каждого отзыва)
lemmatizer = WordNetLemmatizer()  # Создаём объект для лемматизации слов
stop_words = set(stopwords.words('english'))  # Загружаем стоп-слова и преобразуем в set (множество) для быстрого поиска

def preprocess_text(text):
    """
    Функция предварительной обработки текста отзыва:
    - приводит к нижнему регистру
    - удаляет HTML теги
    - оставляет только буквы и пробелы
    - удаляет лишние пробелы
    - разбивает на токены (слова)
    - удаляет стоп-слова
    - применяет лемматизацию
    - возвращает обработанную строку
    """
    
    # Приводим текст к нижнему регистру (чтобы слова 'Good' и 'good' считались одинаковыми)
    text = text.lower()
    
    # Удаляем HTML теги (например <br />, <p>, </div>)
    # re.sub - заменяет найденные по шаблону символы. Шаблон <[^>]+> находит всё, что начинается с < и заканчивается на >
    # ' ' - заменяем теги на пробел (чтобы слова не слипались)
    text = re.sub(r'<[^>]+>', ' ', text)
    
    # Оставляем только буквы a-z, A-Z и пробелы (удаляем цифры, знаки препинания, спецсимволы)
    # [^a-zA-Z\s] - означает "все символы, КРОМЕ букв и пробелов"
    # '' - заменяем найденные символы на пустую строку (удаляем их)
    text = re.sub(r'[^a-zA-Z\s]', '', text)
    
    # Удаляем лишние пробелы (несколько пробелов подряд заменяем на один)
    # \s+ - один или более пробельных символов (пробел, табуляция и т.д.)
    # ' ' - заменяем на один пробел
    text = re.sub(r'\s+', ' ', text)
    
    # Удаляем пробелы в начале и в конце строки (метод strip())
    text = text.strip()
    
    # Разбиваем текст на отдельные слова (токены). Например: "I love movies" -> ["I", "love", "movies"]
    tokens = word_tokenize(text)
    
    # Список comprehension: для каждого токена из tokens 
    # применяем лемматизацию lemmatizer.lemmatize(token) 
    # НО только если токен НЕ входит в стоп-слова (if token not in stop_words)
    tokens = [lemmatizer.lemmatize(token) for token in tokens if token not in stop_words]
    
    # Повторно удаляем стоп-слова (некоторые слова после лемматизации могут стать стоп-словами)
    # Например: 'yours' после лемматизации может стать 'you'
    # Также удаляем слишком короткие слова (len(token) > 2)
    tokens = [token for token in tokens if len(token) > 2 and token not in stop_words]
    
    # Объединяем список токенов обратно в строку, разделяя слова пробелами
    # ' '.join(tokens) - например, ["good", "movie"] -> "good movie"
    return ' '.join(tokens)

def work_with_data(filename):
    """
    Основная функция для обработки данных из файла:
    - читает CSV файл
    - создает столбец label (1 - положительный, 0 - отрицательный)
    - применяет обработку к каждому отзыву
    - сохраняет результат в новый CSV файл
    """
    
    # Читаем CSV файл в DataFrame (таблицу данных)
    # pandas автоматически определит разделитель и заголовки
    data = pd.read_csv(filename)
    
    # Выводим количество загруженных отзывов для информации
    print(f"Загружено {len(data)} отзывов")
    
    # Создаём столбец 'label' на основе существующего столбца 'sentiment'
    # apply - применяет функцию к каждому элементу столбца
    # lambda x: 1 if x == 'positive' else 0 - анонимная функция:
    #   если значение 'positive', возвращаем 1 (положительный)
    #   иначе возвращаем 0 (отрицательный)
    data['label'] = data['sentiment'].apply(lambda x: 1 if x == 'positive' else 0)
    
    # Настраиваем tqdm для отображения прогресс-бара при применении функций к DataFrame
    # progress_apply - аналог apply, но с визуальным прогресс-баром
    # desc - описание, которое будет отображаться над прогресс-баром
    tqdm.pandas(desc="Обработка отзывов")
    
    # Применяем функцию preprocess_text к каждому отзыву в столбце 'review'
    # progress_apply показывает прогресс обработки (сколько отзывов обработано)
    # Результат сохраняем в новый столбец 'processed'
    data['processed'] = data['review'].progress_apply(preprocess_text)
    
    # Формируем имя выходного файла
    # replace('.csv', '_preprocessed.csv') - заменяем расширение .csv на _preprocessed.csv
    # Например: "reviews.csv" -> "reviews_preprocessed.csv"
    output_filename = filename.replace('.csv', '_preprocessed.csv')
    
    # Сохраняем обработанные данные в CSV файл
    # data[['processed', 'label']] - выбираем только два столбца (обработанный текст и метку)
    # index=False - не сохранять индекс строк (чтобы не было лишнего столбца с номерами)
    data[['processed', 'label']].to_csv(output_filename, index=False)
    
    # Выводим первые 5 результатов для проверки (метод head() возвращает первые 5 строк)
    print("\nПримеры обработанных отзывов:")
    print(data[['processed', 'label']].head())
    
    # Проверяем распределение классов (сколько положительных и отрицательных отзывов)
    print(f"\nРаспределение классов:")
    # data['label'].sum() - сумма всех меток (так как положительные = 1, это количество положительных)
    print(f"Положительных (1): {data['label'].sum()}")
    # len(data) - общее количество отзывов, вычитаем положительные, получаем отрицательные
    print(f"Отрицательных (0): {len(data) - data['label'].sum()}")
    
    # Выводим сообщение об успешном сохранении с указанием имени файла
    print(f"\nСохранено в файл: {output_filename}")
    
    # Возвращаем обработанный DataFrame (на случай, если понадобится дальше использовать)
    return data

# Проверяем, что скрипт запущен напрямую (а не импортирован как модуль)
# __name__ - специальная переменная Python
# Если скрипт запущен напрямую, __name__ == '__main__'
if __name__ == "__main__":
    # Вызываем функцию обработки с именем входного файла
    work_with_data("reviews.csv")
